#include <iostream>
#include <string>
#include "NodoC.h"

using namespace std;
template<class T>

NodoC<T>:: NodoC() {
    //dato =0 ;
    next = NULL;
}

template<class T>
NodoC<T>:: NodoC(T a) {
    dato = a;
    next = NULL;
}

template<class T>
T NodoC<T>::getDato() {
    return dato;
}

template<class T>
NodoC<T> * NodoC<T>::getNext() {
    return next;
}

template<class T>
void NodoC<T>::setDato(T a) {
    dato = a;
}

template<class T>
void NodoC<T>::setNext( NodoC<T> *n) {
    next = n;
}

template<class T>
NodoC<T> *NodoC<T>::newNext() {
    next = new  NodoC();
    return next;
}
